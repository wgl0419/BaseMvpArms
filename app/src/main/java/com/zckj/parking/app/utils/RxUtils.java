public class RxUtils {

    private RxUtils() {
    }

    public static <T> ObservableTransformer<HttpResponse<T>, T> applySchedulers(final IView view) {
        return new ObservableTransformer<HttpResponse<T>, T>() {
            @Override
            public Observable<T> apply(Observable<HttpResponse<T>> observable) {
                return observable.subscribeOn(Schedulers.io())
                        .doOnSubscribe(new Consumer<Disposable>() {
                            @Override
                            public void accept(@NonNull Disposable disposable) throws Exception {
                                view.showLoading();//显示进度条
                            }
                        })
                        .subscribeOn(AndroidSchedulers.mainThread())
                        .observeOn(AndroidSchedulers.mainThread())
                        .flatMap(new Function<HttpResponse<T>, Observable<T>>() {
                            @Override
                            public Observable<T> apply(HttpResponse<T> httpResponse) {
                                if ("200".equals(httpResponse.getCode())) {//1000表示请求成功
                                    return createData(httpResponse.getData());
                                } else {
                                    return Observable.error(new ApiException(httpResponse.getMessage(), httpResponse.getCode()));
                                }
                            }
                        })
                        .doFinally(new Action() {
                            @Override
                            public void run() {
                                view.hideLoading();//隐藏进度条
                            }
                        }).compose(RxLifecycleUtils.bindToLifecycle(view));
            }
        };
    }

    /**
     * 此方法已废弃
     *
     * @param view
     * @param <T>
     * @return
     * @see RxLifecycleUtils 此类可以实现 {@link RxLifecycle} 的所有功能, 使用方法和之前一致
     * @deprecated Use {@link RxLifecycleUtils#bindToLifecycle(IView)} instead
     */
    @Deprecated
    public static <T> LifecycleTransformer<T> bindToLifecycle(IView view) {
        return RxLifecycleUtils.bindToLifecycle(view);
    }


    /**
     * 统一线程处理
     *
     * @param <T>
     * @return
     */
    public static <T> ObservableTransformer<T, T> rxSchedulerHelper() {    //compose简化线程
        return new ObservableTransformer<T, T>() {
            @Override
            public ObservableSource<T> apply(Observable<T> observable) {
                return observable.subscribeOn(Schedulers.io())
                        .observeOn(AndroidSchedulers.mainThread());
            }
        };
    }

    /**
     * 统一返回结果处理
     *
     * @param <T>
     * @return
     */
    public static <T> ObservableTransformer<HttpResponse<T>, T> handleResult() {   //compose判断结果

        return new ObservableTransformer<HttpResponse<T>, T>() {
            @Override
            public Observable<T> apply(Observable<HttpResponse<T>> httpResponseFlowable) {
                return httpResponseFlowable.flatMap(new Function<HttpResponse<T>, Observable<T>>() {
                    @Override
                    public Observable<T> apply(HttpResponse<T> httpResponse) {
                        if ("200".equals(httpResponse.getCode())) {//1000表示请求成功
                            return createData(httpResponse.getData());
                        } else {
                            return Observable.error(new ApiException(httpResponse.getMessage(), httpResponse.getCode()));
                        }
                    }
                });
            }
        };
    }

    /**
     * /**
     * 生成Observable
     *
     * @param <T>
     * @return
     */
    public static <T> Observable<T> createData(final T t) {
        return Observable.create(new ObservableOnSubscribe<T>() {
            @Override
            public void subscribe(ObservableEmitter<T> emitter) throws Exception {
                try {
                    emitter.onNext(t);
                    emitter.onComplete();
                } catch (Exception e) {
                    emitter.onError(e);
                }
            }
        });
    }


}
